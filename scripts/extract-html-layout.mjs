// Extract fillable field boxes from templates/contract.html and emit
// pdfLayout/contractLayout.ts for the pdf-lib overlay renderer.
//
// Usage:
//   node scripts/extract-html-layout.mjs
//
// The HTML is the visual source of truth. Every element with data-field becomes
// one overlay field, using its browser layout box converted from CSS px to mm.

import fs from "node:fs";
import path from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath, pathToFileURL } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const PROJECT_ROOT = path.resolve(path.dirname(__filename), "..");

const input = process.argv[2] ?? "templates/contract.html";
const output = process.argv[3] ?? "pdfLayout/contractLayout.ts";

const htmlPath = path.join(PROJECT_ROOT, input);
const outPath = path.join(PROJECT_ROOT, output);
const chromePath =
  process.env.CHROME_BIN ?? "/Applications/Google Chrome.app/Contents/MacOS/Google Chrome";

if (!fs.existsSync(htmlPath)) {
  throw new Error(`Missing HTML template: ${htmlPath}`);
}

const chrome = spawn(chromePath, [
  "--headless=new",
  "--disable-gpu",
  "--no-sandbox",
  "--remote-debugging-port=9225",
  "about:blank",
], {
  stdio: ["ignore", "ignore", "pipe"],
});

let stderr = "";
chrome.stderr.on("data", (chunk) => {
  stderr += chunk.toString();
});

async function main() {
try {
  await waitForChrome();
  const version = await fetchJson("http://127.0.0.1:9225/json/version");
  const cdp = new CdpClient(version.webSocketDebuggerUrl);
  await cdp.open();

  const { targetId } = await cdp.send("Target.createTarget", { url: "about:blank" });
  const { sessionId } = await cdp.send("Target.attachToTarget", { targetId, flatten: true });
  const send = (method, params = {}) => cdp.send(method, params, sessionId);

  await send("Page.enable");
  await send("Runtime.enable");
  await send("Emulation.setDeviceMetricsOverride", {
    width: 794,
    height: 1123,
    deviceScaleFactor: 1,
    mobile: false,
  });

  const pageLoad = cdp.waitFor("Page.loadEventFired", sessionId);
  await send("Page.navigate", { url: pathToFileURL(htmlPath).href });
  await pageLoad;

  const { result } = await send("Runtime.evaluate", {
    awaitPromise: true,
    returnByValue: true,
    expression: `(${extractFieldsInPage.toString()})()`,
  });

  const fields = result.value;
  if (!Array.isArray(fields) || fields.length === 0) {
    throw new Error("No data-field elements found in HTML template");
  }

  writeLayout(fields, input, outPath);
  console.log(`Wrote ${outPath} (${fields.length} fields from ${input})`);
} finally {
  chrome.kill("SIGTERM");
}
}

function extractFieldsInPage() {
  function alignFor(el) {
    if (el.classList.contains("center") || el.classList.contains("cnp-cell")) return "center";
    if (el.classList.contains("garantii")) return "left";
    if (el.closest(".company-block")) return "left";
    return "center";
  }

  function fontFor(el) {
    const weight = getComputedStyle(el).fontWeight;
    return Number(weight) >= 600 || el.classList.contains("bold") ? "bold" : "regular";
  }

  function fieldSize(el) {
    const size = Number.parseFloat(getComputedStyle(el).fontSize);
    return Math.round(size * 0.75 * 10) / 10;
  }

  function round(n) {
    return Math.round(n * 1000) / 1000;
  }

  return document.fonts.ready.then(() => {
    const page = document.querySelector(".page");
    if (!page) throw new Error("Missing .page element");

    const pxProbe = document.createElement("div");
    pxProbe.style.cssText = "position:absolute;left:-9999px;top:-9999px;width:100mm;height:1mm;";
    document.body.appendChild(pxProbe);
    const pxPerMm = pxProbe.getBoundingClientRect().width / 100;
    pxProbe.remove();

    return [...document.querySelectorAll("[data-field]")].map((el) => {
      const rect = el.getBoundingClientRect();
      const multiline = el.classList.contains("garantii");
      const isCnp = el.classList.contains("cnp-cell");
      const isBoxed = el.classList.contains("boxed-field");
      const isSigBox = el.classList.contains("signature-box");
      const isUnderline = el.classList.contains("underline");
      const isTableField = !!el.closest("table");

      return {
        name: el.getAttribute("data-field"),
        kind: "text",
        x: round(rect.left / pxPerMm),
        y: round(rect.top / pxPerMm),
        w: round(rect.width / pxPerMm),
        h: round(rect.height / pxPerMm),
        fontSize: isCnp ? 9 : fieldSize(el),
        font: fontFor(el),
        align: alignFor(el),
        valign: multiline ? "bottom" : isCnp || isBoxed || isSigBox || isTableField ? "middle" : isUnderline ? "bottom" : "bottom",
        lineHeight: 1,
        multiline,
      };
    });
  });
}

function writeLayout(fields, inputName, outputPath) {
  const out = [];
  out.push(`// Generated by scripts/extract-html-layout.mjs from ${inputName}`);
  out.push(`// ${fields.length} fields; regenerated ${new Date().toISOString()}`);
  out.push(`// Source of truth: templates/contract.html + templates/contract.css`);
  out.push(`// Do not edit by hand - re-run the script after touching the HTML/CSS template.`);
  out.push(``);
  out.push(`import type { Field } from "./types";`);
  out.push(``);
  out.push(`export const FIELDS: Field[] = [`);

  for (const f of fields) {
    const multiline = f.multiline ? ", multiline: true" : "";
    out.push(
      `  { name: ${JSON.stringify(f.name)}, kind: "text", x: ${f.x}, y: ${f.y}, w: ${f.w}, h: ${f.h}, fontSize: ${f.fontSize}, font: ${JSON.stringify(f.font)}, align: ${JSON.stringify(f.align)}, valign: ${JSON.stringify(f.valign)}, lineHeight: ${f.lineHeight}${multiline} },`,
    );
  }

  out.push(`];`);
  out.push(``);
  out.push(`export const FIELDS_BY_NAME: Map<string, Field> = new Map(FIELDS.map((f) => [f.name, f]));`);
  out.push(``);
  out.push(`export function byName(name: string): Field | undefined {`);
  out.push(`  return FIELDS_BY_NAME.get(name);`);
  out.push(`}`);
  out.push(``);
  out.push(`export function cnpCells(): Field[] {`);
  out.push(`  const out: Field[] = [];`);
  out.push(`  for (let i = 1; i <= 13; i++) {`);
  out.push(`    const f = FIELDS_BY_NAME.get(\`CNP_\${i}\`);`);
  out.push(`    if (f) out.push(f);`);
  out.push(`  }`);
  out.push(`  return out;`);
  out.push(`}`);
  out.push(``);
  out.push(`// Stamp overlay — kept out of FIELDS so the HTML extractor doesn't overwrite it.`);
  out.push(`// Anchor matches the position the legacy base PDF used (x≈167–198mm, y≈137–180mm).`);
  out.push(`export const STAMP_FIELD: Field = {`);
  out.push(`  name: "STAMP",`);
  out.push(`  kind: "image",`);
  out.push(`  imageName: "stamp-dogar",`);
  out.push(`  x: 172,`);
  out.push(`  y: 145,`);
  out.push(`  w: 31,`);
  out.push(`  h: 31,`);
  out.push(`  fontSize: 0,`);
  out.push(`  font: "regular",`);
  out.push(`  align: "left",`);
  out.push(`  valign: "top",`);
  out.push(`};`);

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, out.join("\n") + "\n");
}

async function waitForChrome() {
  const started = Date.now();
  while (Date.now() - started < 10000) {
    try {
      await fetchJson("http://127.0.0.1:9225/json/version");
      return;
    } catch {
      await new Promise((resolve) => setTimeout(resolve, 100));
    }
  }
  throw new Error(`Chrome did not start. stderr:\n${stderr}`);
}

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${url}: ${res.status}`);
  return await res.json();
}

class CdpClient {
  constructor(url) {
    this.url = url;
    this.nextId = 1;
    this.pending = new Map();
    this.waiters = [];
  }

  async open() {
    this.ws = new WebSocket(this.url);
    this.ws.addEventListener("message", (event) => this.onMessage(JSON.parse(event.data)));
    await new Promise((resolve, reject) => {
      this.ws.addEventListener("open", resolve, { once: true });
      this.ws.addEventListener("error", reject, { once: true });
    });
  }

  send(method, params = {}, sessionId) {
    const id = this.nextId++;
    const message = { id, method, params };
    if (sessionId) message.sessionId = sessionId;
    this.ws.send(JSON.stringify(message));
    return new Promise((resolve, reject) => {
      this.pending.set(id, { resolve, reject });
    });
  }

  waitFor(method, sessionId) {
    return new Promise((resolve) => {
      this.waiters.push({ method, sessionId, resolve });
    });
  }

  onMessage(message) {
    if (message.id) {
      const pending = this.pending.get(message.id);
      if (!pending) return;
      this.pending.delete(message.id);
      if (message.error) pending.reject(new Error(message.error.message));
      else pending.resolve(message.result ?? {});
      return;
    }

    for (let i = 0; i < this.waiters.length; i++) {
      const waiter = this.waiters[i];
      if (waiter.method === message.method && waiter.sessionId === message.sessionId) {
        this.waiters.splice(i, 1);
        waiter.resolve(message.params ?? {});
        return;
      }
    }
  }
}

await main();
